/**
 * Inicializa ADC sequencer 3, canal 9
 */
void adc_init(void) {
  ADC0_InitSWTriggerSeq3_Ch9();		// Sequencer 3, canal 9 (PE4)
}

/**
 * Realiza leitura do ADC sequencer 3, canal 9
 * 
 * @return uint32_t - valor lido pelo ADC
 */
uint32_t adc_get_value(void) {
  return ADC0_InSeq3();
}
